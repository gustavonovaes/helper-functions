<?php

function cleanAccents(string $string): string
{
  return \strtr($string, [
    'Á' => 'A', 'À' => 'A', 'Ã' => 'A',
    'á' => 'a', 'à' => 'a', 'ã' => 'a',
    'É' => 'E', 'È' => 'E', 'Ẽ' => 'E',
    'é' => 'e', 'è' => 'e', 'ẽ' => 'e',
    'Í' => 'I', 'Ì' => 'I',
    'í' => 'i', 'ì' => 'i',
    'Ó' => 'O', 'Ò' => 'O', 'Õ' => 'O',
    'ó' => 'o', 'ò' => 'o', 'õ' => 'o',
    'Ú' => 'U', 'Ù' => 'U',
    'ú' => 'u', 'ù' => 'u',
    'Ç' => 'C',
    'ç' => 'c',
  ]);
}
